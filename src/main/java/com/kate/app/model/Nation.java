package com.kate.app.model;


public class Nation {
	private int id;
	private String nation_name;
	private String nation_num;
	private String center_gps;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNation_name() {
		return nation_name;
	}
	public void setNation_name(String nation_name) {
		this.nation_name = nation_name;
	}
	public String getNation_num() {
		return nation_num;
	}
	public void setNation_num(String nation_num) {
		this.nation_num = nation_num;
	}
	public String getCenter_gps() {
		return center_gps;
	}
	public void setCenter_gps(String center_gps) {
		this.center_gps = center_gps;
	}
	

}
