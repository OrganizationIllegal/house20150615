package com.kate.app.model;

public class NearFacilitie {
	private int id;
	private int traffic_mark;
	private String school_info;
	private int walk_mark;
	private int school_distance;
	private String market_info;
	private int market_distance;
	private int house_id;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getTraffic_mark() {
		return traffic_mark;
	}
	public void setTraffic_mark(int traffic_mark) {
		this.traffic_mark = traffic_mark;
	}
	public String getSchool_info() {
		return school_info;
	}
	public void setSchool_info(String school_info) {
		this.school_info = school_info;
	}
	public int getWalk_mark() {
		return walk_mark;
	}
	public void setWalk_mark(int walk_mark) {
		this.walk_mark = walk_mark;
	}
	public int getSchool_distance() {
		return school_distance;
	}
	public void setSchool_distance(int school_distance) {
		this.school_distance = school_distance;
	}
	public String getMarket_info() {
		return market_info;
	}
	public void setMarket_info(String market_info) {
		this.market_info = market_info;
	}
	public int getMarket_distance() {
		return market_distance;
	}
	public void setMarket_distance(int market_distance) {
		this.market_distance = market_distance;
	}
	public int getHouse_id() {
		return house_id;
	}
	public void setHouse_id(int house_id) {
		this.house_id = house_id;
	}
}
