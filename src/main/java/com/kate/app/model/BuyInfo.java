package com.kate.app.model;

public class BuyInfo {
	private int id;
	private int first_money;
	private int stamp_tax;
	private int return_money;
	private int lawyer_fee;
	private int house_info_id;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getFirst_money() {
		return first_money;
	}
	public void setFirst_money(int first_money) {
		this.first_money = first_money;
	}
	public int getStamp_tax() {
		return stamp_tax;
	}
	public void setStamp_tax(int stamp_tax) {
		this.stamp_tax = stamp_tax;
	}
	public int getReturn_money() {
		return return_money;
	}
	public void setReturn_money(int return_money) {
		this.return_money = return_money;
	}
	public int getLawyer_fee() {
		return lawyer_fee;
	}
	public void setLawyer_fee(int lawyer_fee) {
		this.lawyer_fee = lawyer_fee;
	}
	public int getHouse_info_id() {
		return house_info_id;
	}
	public void setHouse_info_id(int house_info_id) {
		this.house_info_id = house_info_id;
	}
	
}
